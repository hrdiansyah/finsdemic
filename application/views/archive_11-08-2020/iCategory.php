<p class="pleft"><strong>Maksud dan Tujuan</strong><br />  Sehubungan dengan target penyaluran pinjaman SMF yang tertuang di Rencana Anggaran Kerja Perusahaan (RKAP) tahun 2020 sebesar Rp 13 Triliun kepada mitra lembaga penyalur Kredit Perumahan Rakyat (KPR), yaitu:<br />
a) Bank Umum <br />
b) Bank Pembangunan Daerah <br />
c) Bank Syariah, dan <br />
d) Perusahaan Pembiayaan</p>

<p class="pleft">maka diperlukan solusi dan kerjasama dengan perusahaan Asuransi untuk pengelolaan risiko Kredit Modal Kerja (KMK). Program Asuransi KMK ini dimaksudkan untuk memproteksi SMF dari kemungkinan Kredit Macet atau Gagal Bayar dari mitra lembaga penyalur KPR termasuk untuk bentuk-bentuk pembiayaan lain yang akan dijalankan oleh SMF kedepan.</p>

<p class="pleft"><strong>Skema Kerjasama SMF dengan Lembaga Penyalur KPR </strong> Sebagai Special Mission Vehicle (SMV) dibawah Kementerian Keuangan yang mengemban misi khusus dalam hal penyedia dana jangka panjang untuk penyaluran KPR, maka SMF memiliki 3 pilar bisnis utama yaitu <strong>Sekuritisasi, Penyaluran Pinjaman &amp; Pembelian KPR.</strong></p>

